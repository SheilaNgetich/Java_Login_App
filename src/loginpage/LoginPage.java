    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        try {
            try {
                //registering the drivers
                Class.forName("com.mysql.cj.jdbc.Driver");
            } catch (ClassNotFoundException ex) {
                System.out.println(ex.getMessage());
            }
            //creating a connection
            String url = "jdbc:mysql://localhost:3306/java_user_database";
            String user = "root";
            String passwordcon = "";
            
            
            Connection con = DriverManager.getConnection(url, user, passwordcon);
            System.out.println("Database connected successfully");
            
            //Getting the username and password
            String username = txtUser.getText();
            char [] arr = userPass.getPassword();
            String password =String.valueOf(arr);
            
            //creating a statement
            Statement st = con.createStatement();
            
            //executing sql queries
            String query ="select * from login WHERE username = '" + username + "' AND password = '" + password + "'";
            ResultSet resultSet =st.executeQuery(query);
            
            //iterating through the result set
            if (resultSet.next()) {
                //if username and password are correct, proceed to home page
                dispose();
                //Opening the home page via its instance
                HomePage hp =  new HomePage();
                hp.setVisible(true);
                //displaying success message
                JOptionPane.showMessageDialog(hp, "You have successfully logged in");
            }else{
                //if username and password are incorrect
                //values will reset
               txtUser.setText("");
               userPass.setText("");
               JOptionPane.showMessageDialog(this, "Invalid username or password", "Login Failed", JOptionPane.ERROR_MESSAGE);
            }
            
            //closing the connection
            con.close();
            

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_loginbtnActionPerformed

